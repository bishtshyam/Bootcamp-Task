namespace MathOperations;
public class Class1
{
    public static int add(int num1 , int num2)
    {
        return num1 + num2;
    }

    public static int subtract(int num1, int num2)
    {
        return num2 - num1;
    }
    
    public static string join_string(string str1, string str2)
    {
        return str1 + str2;
    }

}
